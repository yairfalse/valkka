// Commit graph — interactive Canvas 2D rendering of the DAG.
// Warm Nordic palette. Agent commits get square nodes.

const LANE_COLORS = [
  "#c8a050", "#e5534b", "#5ec4b6", "#a78bfa",
  "#5b8def", "#f472b6", "#3ecf8e", "#fb923c",
]

const CONF = {
  rowHeight: 32,
  laneWidth: 18,
  nodeRadius: 3.5,
  mergeSize: 4.5,
  agentSize: 5,
  leftPad: 14,
  font: "12px 'Inter', -apple-system, BlinkMacSystemFont, system-ui, sans-serif",
  monoFont: "11px 'SF Mono', 'Cascadia Code', 'JetBrains Mono', ui-monospace, monospace",
  tagFont: "10px 'Inter', -apple-system, BlinkMacSystemFont, system-ui, sans-serif",
  bg: "#0e0d0b",
  textColor: "#e8e4de",
  dimColor: "#5a5448",
  mutedColor: "#8a8278",
  hoverBg: "rgba(255,240,200,0.03)",
  selectedBg: "rgba(255,240,200,0.06)",
  branchBg: "rgba(200,160,80,0.08)",
  branchColor: "#c8a050",
  branchBorder: "rgba(200,160,80,0.2)",
  agentColor: "#3ecf8e",
  laneLineWidth: 1.5,
  laneLineAlpha: 0.25,
}

function laneColor(col) {
  return LANE_COLORS[col % LANE_COLORS.length]
}

function laneX(col) {
  return CONF.leftPad + col * CONF.laneWidth
}

function rowY(row) {
  return 22 + row * CONF.rowHeight
}

function relativeTime(iso) {
  const d = new Date(iso)
  const diff = Date.now() - d.getTime()
  const mins = Math.floor(diff / 60000)
  if (mins < 1) return "now"
  if (mins < 60) return `${mins}m`
  const hrs = Math.floor(mins / 60)
  if (hrs < 24) return `${hrs}h`
  const days = Math.floor(hrs / 24)
  if (days < 30) return `${days}d`
  const months = Math.floor(days / 30)
  return `${months}mo`
}

function drawRoundRect(ctx, x, y, w, h, r) {
  ctx.beginPath()
  if (ctx.roundRect) {
    ctx.roundRect(x, y, w, h, r)
  } else {
    ctx.moveTo(x + r, y)
    ctx.lineTo(x + w - r, y)
    ctx.arcTo(x + w, y, x + w, y + r, r)
    ctx.lineTo(x + w, y + h - r)
    ctx.arcTo(x + w, y + h, x + w - r, y + h, r)
    ctx.lineTo(x + r, y + h)
    ctx.arcTo(x, y + h, x, y + h - r, r)
    ctx.lineTo(x, y + r)
    ctx.arcTo(x, y, x + r, y, r)
    ctx.closePath()
  }
}

function isAgentCommit(node) {
  if (!node.message) return false
  const msg = node.message.toLowerCase()
  return msg.includes("co-authored-by: claude") ||
    msg.includes("generated by") ||
    msg.includes("aider") ||
    msg.includes("cursor") ||
    (node.author && /\b(claude|aider|copilot|cursor)\b/i.test(node.author))
}

function clearCanvas(canvas) {
  const ctx = canvas.getContext("2d")
  ctx.setTransform(1, 0, 0, 1, 0, 0)
  ctx.clearRect(0, 0, canvas.width, canvas.height)
  canvas.width = 0
  canvas.height = 0
  canvas.style.width = "0"
  canvas.style.height = "0"
}

function render(canvas, data, hoverRow, selectedRow) {
  const ctx = canvas.getContext("2d")
  ctx.setTransform(1, 0, 0, 1, 0, 0)
  ctx.clearRect(0, 0, canvas.width, canvas.height)

  if (!data || !data.nodes || data.nodes.length === 0) {
    canvas.width = 0
    canvas.height = 0
    return
  }

  const dpr = window.devicePixelRatio || 1
  const graphWidth = CONF.leftPad + (data.max_columns + 1) * CONF.laneWidth
  const textAreaStart = graphWidth + 14
  const totalWidth = Math.max(textAreaStart + 560, 680)
  const totalHeight = 44 + data.nodes.length * CONF.rowHeight

  canvas.width = totalWidth * dpr
  canvas.height = totalHeight * dpr
  canvas.style.width = totalWidth + "px"
  canvas.style.height = totalHeight + "px"

  ctx.setTransform(dpr, 0, 0, dpr, 0, 0)

  // Background
  ctx.fillStyle = CONF.bg
  ctx.fillRect(0, 0, totalWidth, totalHeight)

  // Draw hover/selected row highlights
  for (const node of data.nodes) {
    const y = rowY(node.row)
    if (node.row === selectedRow) {
      ctx.fillStyle = CONF.selectedBg
      ctx.fillRect(0, y - CONF.rowHeight / 2, totalWidth, CONF.rowHeight)
    } else if (node.row === hoverRow) {
      ctx.fillStyle = CONF.hoverBg
      ctx.fillRect(0, y - CONF.rowHeight / 2, totalWidth, CONF.rowHeight)
    }
  }

  // Build oid -> node lookup
  const nodeByOid = {}
  for (const n of data.nodes) {
    nodeByOid[n.oid] = n
  }

  const laneSnapshots = data.active_lanes_per_row || []

  // 1) Lane continuity lines
  for (let row = 0; row < laneSnapshots.length - 1; row++) {
    const activeCols = laneSnapshots[row]
    const nextActiveCols = new Set(laneSnapshots[row + 1] || [])
    if (!activeCols) continue

    const y1 = rowY(row)
    const y2 = rowY(row + 1)

    for (const col of activeCols) {
      if (!nextActiveCols.has(col)) continue

      const x = laneX(col)
      ctx.globalAlpha = CONF.laneLineAlpha
      ctx.strokeStyle = laneColor(col)
      ctx.lineWidth = CONF.laneLineWidth
      ctx.beginPath()
      ctx.moveTo(x, y1)
      ctx.lineTo(x, y2)
      ctx.stroke()
    }
  }
  ctx.globalAlpha = 1.0

  // 2) Cross-lane edges
  for (const edge of data.edges) {
    if (edge.from_col === edge.to_col) continue

    const fromX = laneX(edge.from_col)
    const fromY = rowY(edge.from_row)
    const toX = laneX(edge.to_col)
    const toNode = nodeByOid[edge.to_oid]
    const toY = toNode ? rowY(toNode.row) : fromY + CONF.rowHeight

    ctx.strokeStyle = laneColor(edge.to_col)
    ctx.lineWidth = CONF.laneLineWidth
    ctx.globalAlpha = 0.5
    ctx.beginPath()
    ctx.moveTo(fromX, fromY)

    const midY = (fromY + toY) / 2
    ctx.bezierCurveTo(fromX, midY, toX, midY, toX, toY)
    ctx.stroke()
  }
  ctx.globalAlpha = 1.0

  // 3) Nodes and labels
  for (const node of data.nodes) {
    const x = laneX(node.column)
    const y = rowY(node.row)
    const color = laneColor(node.column)
    const isSelected = node.row === selectedRow
    const isHover = node.row === hoverRow
    const isAgent = isAgentCommit(node)

    // Node shape
    if (node.is_merge) {
      // Diamond for merges
      const s = CONF.mergeSize
      ctx.fillStyle = CONF.bg
      ctx.strokeStyle = color
      ctx.lineWidth = 1.5
      ctx.beginPath()
      ctx.moveTo(x, y - s)
      ctx.lineTo(x + s, y)
      ctx.lineTo(x, y + s)
      ctx.lineTo(x - s, y)
      ctx.closePath()
      ctx.fill()
      ctx.stroke()
    } else if (isAgent) {
      // Square for agent commits — amber accent
      const s = isSelected ? CONF.agentSize + 1 : (isHover ? CONF.agentSize + 0.5 : CONF.agentSize)
      const half = s / 2
      ctx.fillStyle = CONF.agentColor
      ctx.fillRect(x - half, y - half, s, s)

      if (isSelected) {
        ctx.strokeStyle = "#e8e4de"
        ctx.lineWidth = 1.5
        ctx.strokeRect(x - half - 2, y - half - 2, s + 4, s + 4)
      }
    } else {
      // Circle for human commits
      const r = isSelected ? CONF.nodeRadius + 1.5 : (isHover ? CONF.nodeRadius + 0.5 : CONF.nodeRadius)
      ctx.fillStyle = isSelected ? color : (isHover ? color : color)
      ctx.globalAlpha = isAgent ? 1.0 : 0.7
      ctx.beginPath()
      ctx.arc(x, y, r, 0, Math.PI * 2)
      ctx.fill()
      ctx.globalAlpha = 1.0

      if (isSelected) {
        ctx.strokeStyle = "#e8e4de"
        ctx.lineWidth = 1.5
        ctx.beginPath()
        ctx.arc(x, y, r + 2, 0, Math.PI * 2)
        ctx.stroke()
      }
    }

    // Text layout
    let tx = textAreaStart
    ctx.font = CONF.monoFont

    // Short OID
    ctx.fillStyle = isSelected ? color : CONF.dimColor
    ctx.fillText(node.short_oid, tx, y + 4)
    tx += 68

    // Branch badges
    ctx.font = CONF.font
    if (node.branches && node.branches.length > 0) {
      for (const br of node.branches) {
        const tw = ctx.measureText(br).width + 10
        ctx.fillStyle = CONF.branchBg
        drawRoundRect(ctx, tx - 2, y - 8, tw, 16, 3)
        ctx.fill()
        ctx.strokeStyle = CONF.branchBorder
        ctx.lineWidth = 1
        drawRoundRect(ctx, tx - 2, y - 8, tw, 16, 3)
        ctx.stroke()
        ctx.fillStyle = CONF.branchColor
        ctx.fillText(br, tx + 3, y + 4)
        tx += tw + 5
      }
      tx += 3
    }

    // Agent tag
    if (isAgent && !isSelected) {
      const tag = "agent"
      const tagW = ctx.measureText(tag).width + 8
      ctx.fillStyle = "rgba(62,207,142,0.08)"
      drawRoundRect(ctx, tx - 1, y - 7, tagW, 14, 3)
      ctx.fill()
      ctx.fillStyle = CONF.agentColor
      ctx.globalAlpha = 0.6
      ctx.font = CONF.tagFont
      ctx.fillText(tag, tx + 3, y + 3)
      ctx.globalAlpha = 1.0
      ctx.font = CONF.font
      tx += tagW + 4
    }

    // Message
    const msgEnd = textAreaStart + 400
    const msgMaxW = Math.max(msgEnd - tx, 60)
    ctx.fillStyle = isSelected ? "#e8e4de" : CONF.textColor
    ctx.globalAlpha = isAgent ? 0.7 : 1.0
    ctx.font = CONF.font
    let msg = node.message || ""
    if (ctx.measureText(msg).width > msgMaxW) {
      while (msg.length > 0 && ctx.measureText(msg + "…").width > msgMaxW) {
        msg = msg.slice(0, -1)
      }
      msg += "…"
    }
    ctx.fillText(msg, tx, y + 4)
    ctx.globalAlpha = 1.0

    // Author
    ctx.fillStyle = isSelected ? CONF.mutedColor : CONF.dimColor
    ctx.fillText(node.author || "", textAreaStart + 410, y + 4)

    // Time
    ctx.fillStyle = CONF.dimColor
    ctx.font = CONF.monoFont
    ctx.fillText(relativeTime(node.timestamp), textAreaStart + 510, y + 4)
  }
}

function getRowFromY(y, data) {
  if (!data || !data.nodes || data.nodes.length === 0) return -1
  const half = CONF.rowHeight / 2
  for (const node of data.nodes) {
    const ny = rowY(node.row)
    if (y >= ny - half && y < ny + half) {
      return node.row
    }
  }
  return -1
}

function getCanvasY(canvas, clientY) {
  const rect = canvas.getBoundingClientRect()
  return clientY - rect.top
}

const GraphHook = {
  mounted() {
    this.canvas = this.el
    this.data = null
    this.hoverRow = -1
    this.selectedRow = -1

    this.canvas.style.cursor = "pointer"

    this.canvas.addEventListener("mousemove", (e) => {
      if (!this.data) return
      const y = getCanvasY(this.canvas, e.clientY)
      const row = getRowFromY(y, this.data)
      if (row !== this.hoverRow) {
        this.hoverRow = row
        render(this.canvas, this.data, this.hoverRow, this.selectedRow)
      }
    })

    this.canvas.addEventListener("mouseleave", () => {
      if (this.hoverRow !== -1) {
        this.hoverRow = -1
        render(this.canvas, this.data, this.hoverRow, this.selectedRow)
      }
    })

    this.canvas.addEventListener("click", (e) => {
      if (!this.data) return
      const y = getCanvasY(this.canvas, e.clientY)
      const row = getRowFromY(y, this.data)
      if (row === -1) return

      const node = this.data.nodes.find(n => n.row === row)
      if (!node) return

      if (this.selectedRow === row) {
        this.selectedRow = -1
        this.pushEvent("graph:deselect_commit", {})
      } else {
        this.selectedRow = row
        this.pushEvent("graph:select_commit", {
          oid: node.oid,
          short_oid: node.short_oid,
          message: node.message,
          author: node.author,
          timestamp: node.timestamp,
          branches: node.branches || [],
          is_merge: node.is_merge,
          parents: node.parents || [],
        })
      }
      render(this.canvas, this.data, this.hoverRow, this.selectedRow)
    })

    this.handleEvent("graph:update", (data) => {
      this.data = data
      this.selectedRow = -1
      this.hoverRow = -1
      render(this.canvas, this.data, this.hoverRow, this.selectedRow)
    })

    this.handleEvent("graph:clear", () => {
      this.data = null
      this.selectedRow = -1
      this.hoverRow = -1
      clearCanvas(this.canvas)
    })
  },

  updated() {},
  destroyed() {},
}

export default GraphHook
